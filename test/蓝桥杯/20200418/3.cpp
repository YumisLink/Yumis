#include<cstdio>
int A(int x, int y)
{
    int ans = 1;;
    for (int i = 1; i <= x; i ++)
    {
        ans *= i;
    }
    for (int i = 1; i <= x-y; i ++)
    {
        ans /= i;
    }
    return ans;
}
int main ()
{
    printf("%d",A(6,6) + A(5,5)* (5+4+3+2+1));
}



